int	ft_str_is_numeric(char *str)
{
	int i;

	i = 0;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i])
	{
		if (str[i] == '.')
			i++;
		if (!(str[i] >= 48 && str[i] < 58))
			return (0);
		i++;
	}
	return (1);
}
